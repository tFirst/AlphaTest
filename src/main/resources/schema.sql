CREATE TABLE Brands
(ID_BRAND BIGINT PRIMARY KEY AUTO_INCREMENT,
TITLE VARCHAR(100));

CREATE TABLE Types
(ID_TYPE BIGINT PRIMARY KEY AUTO_INCREMENT,
TITLE VARCHAR(100));

CREATE TABLE Features
(ID_FEATURE BIGINT PRIMARY KEY AUTO_INCREMENT,
TITLE VARCHAR(100));

CREATE TABLE Products
(ID_PRODUCT BIGINT PRIMARY KEY AUTO_INCREMENT,
TYPE_ID BIGINT REFERENCES Types(ID_TYPE),
BRAND_ID BIGINT REFERENCES Brands(ID_BRAND),
TITLE VARCHAR(100),
COUNT BIGINT,
PRICE BIGINT);

CREATE TABLE Product_Features
(ID BIGINT PRIMARY KEY AUTO_INCREMENT,
PRODUCT_ID BIGINT REFERENCES Products(ID_PRODUCT),
FEATURE_ID BIGINT REFERENCES Features(ID_FEATURE),
VALUE VARCHAR(100));